public class Strings {
    public static void main(String[] args) {

        // char.
        char singleLetter = 'M';
        System.out.println(singleLetter);

        // String.
        String myName = "Mateo";
        System.out.println(myName);

        // new String.
        String myLastName = new String("Rendon");
        System.out.println(myLastName);
    }
}
